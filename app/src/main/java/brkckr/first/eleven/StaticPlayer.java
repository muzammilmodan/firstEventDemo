package brkckr.first.eleven;

public class StaticPlayer
{
    public String name;
    public String profilePictureUrl;
    public int number;

    public StaticPlayer(String name, String profilePictureUrl, int number)
    {
        this.name = name;
        this.profilePictureUrl = profilePictureUrl;
        this.number = number;
    }
}
